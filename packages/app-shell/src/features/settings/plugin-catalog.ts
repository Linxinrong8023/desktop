/** A plugin's brand mark. Tabler icons and the hand-drawn marks both satisfy this. */
export type PluginMark = ComponentType<{ className?: string }>;

/** Whether a plugin comes from the shared marketplace or the signed-in user's own space. */
export type PluginCollection = "public" | "personal";

export interface PluginEntry {
  id: string;
  /** Product names are proper nouns, so they stay untranslated. */
  name: string;
  publisher: string;
  /** Reverse-DNS id shown verbatim in the detail pane's info table. */
  identifier: string;
  version: string;
  /** ISO date formatted with the active locale, which keeps it out of the translation catalog. */
  updated: string;
  size: string;
  collection: PluginCollection;
  /** Featured plugins lead the browse grid before the rest of the collection. */
  featured: boolean;
  categoryKey: string;
  capabilityKeys: string[];
  mark: PluginMark;
  /** Tailwind classes tinting the icon tile so brands stay distinguishable at 24px. */
  tint: string;
  summaryKey: string;
  /** Skill names surfaced in the detail pane; only the first three plugins ship more than one. */
  skills: string[];
}

